#perimeter of the Rectangle
#Perimeter of rectangle formula is 2(L+W)

length=float(input("Enter the Length of the Rectangle: "))
width=float(input("Enter the width of the Rectangle: "))
peri=2*(length+width)
print('='*50)
print("Perimeter of the Rectangle is: {} square units".format(peri))
print('='*50)

