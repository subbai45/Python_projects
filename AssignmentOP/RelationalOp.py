#program to demonstrate relational operators

a=input("Enter the first value: ")
b=input("Enter the second value: ")

print("="*40)
print("\tRelational Operators")
print("="*40)
print("\t\t{} > {} = {}".format(a,b,a>b))
print("\t\t{} < {} = {}".format(a,b,a<b))
print("\t\t{} == {} = {}".format(a,b,a==b))
print("\t\t{} != {} = {}".format(a,b,a!=b))
print("\t\t{} >= {} = {}".format(a,b,a>=b))
print("\t\t{} <= {} = {}".format(a,b,a<=b))
print("="*40)