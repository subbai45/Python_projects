#Demonstrating all arithemetic operations

a=int(input("enter the first value:- "))
b=int(input("enter the second value:- "))
print("="*50)
print("\tAll Arithemetic Operations")
print("="*50)
print("\tSum of ({},{}) = {}".format(a,b,a+b))
print("\tSubtraction of ({},{}) = {}".format(a,b,a-b))
print("\tMultiplication of ({},{}) = {}".format(a,b,a*b))
print("\tNormal division of ({},{}) = {}".format(a,b,a/b))
print("\tFloor Division of ({},{}) = {}".format(a,b,a//b))
print("\tExponential of ({},{}) = {}".format(a,b,a**b))
print("="*50)